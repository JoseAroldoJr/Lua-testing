package Objetos;

public class Condicao {
    private String Tipo;

    public String getTipo() {
        return Tipo;
    }

    public void setTipo(String Tipo) {
        this.Tipo = Tipo;
    }

    @Override
    public String toString() {
        return "Condicoes{" + "Tipo=" + Tipo + '}';
    }
    
}
